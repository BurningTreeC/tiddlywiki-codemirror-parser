import resolve from "@rollup/plugin-node-resolve";
import commonjs from "@rollup/plugin-commonjs";
import terser from "@rollup/plugin-terser";

function twBanner(title) {
  return `/*\\
title: ${title}
type: application/javascript
module-type: library

Bundled CodeMirror 6 library module.
DO NOT EDIT – generated by Rollup.

\\*/
/*jslint node: true, browser: true */
"use strict";
`;
}

const twPaths = {
  "@codemirror/state":
    "$:/plugins/BurningTreeC/tiddlywiki-codemirror/lib/codemirror-state.js",
  "@codemirror/view":
    "$:/plugins/BurningTreeC/tiddlywiki-codemirror/lib/codemirror-view.js",
  "@codemirror/commands":
    "$:/plugins/BurningTreeC/tiddlywiki-codemirror/lib/codemirror-commands.js",
  "@codemirror/language":
    "$:/plugins/BurningTreeC/tiddlywiki-codemirror/lib/codemirror-language.js",
  "@codemirror/autocomplete":
    "$:/plugins/BurningTreeC/tiddlywiki-codemirror/lib/codemirror-autocomplete.js",
  "@codemirror/lang-html":
    "$:/plugins/BurningTreeC/tiddlywiki-codemirror/lib/codemirror-lang-html.js",
  "@lezer/common":
    "$:/plugins/BurningTreeC/tiddlywiki-codemirror/lib/lezer-common.js",
  "@lezer/highlight":
    "$:/plugins/BurningTreeC/tiddlywiki-codemirror/lib/lezer-highlight.js"
};

const libs = [
  { name: "codemirror-state", input: "@codemirror/state" },
  { name: "codemirror-view", input: "@codemirror/view" },
  { name: "codemirror-commands", input: "@codemirror/commands" },
  { name: "codemirror-language", input: "@codemirror/language" },
  { name: "codemirror-autocomplete", input: "@codemirror/autocomplete" },
  { name: "codemirror-lang-html", input: "@codemirror/lang-html" },
  { name: "lezer-common", input: "@lezer/common" },
  { name: "lezer-highlight", input: "@lezer/highlight" }
];

// Build each lib, but keep cross-lib deps external so we only have ONE instance.
function externalsFor(libName) {
  const all = Object.keys(twPaths);

  // The lib itself must NOT be external (otherwise it would just `require()` itself)
  // Example: building codemirror-view => keep @codemirror/view internal, but externalize @codemirror/state etc.
  const selfPkg =
    libName.startsWith("codemirror-") ? "@codemirror/" + libName.replace("codemirror-", "")
    : libName.startsWith("lezer-") ? "@lezer/" + libName.replace("lezer-", "")
    : null;

  return all.filter((pkg) => pkg !== selfPkg);
}

export default libs.map((lib) => {
  const pkgName =
    lib.name.startsWith("codemirror-")
      ? "@codemirror/" + lib.name.replace("codemirror-", "")
      : lib.name.startsWith("lezer-")
        ? "@lezer/" + lib.name.replace("lezer-", "")
        : lib.input;

  return {
    input: lib.input,
    external: externalsFor(lib.name),
    output: {
      file: `dist/plugins/editor/files/lib/${lib.name}.js`,
      format: "cjs",
      exports: "named",
      banner: twBanner(`$:/plugins/BurningTreeC/tiddlywiki-codemirror/lib/${lib.name}.js`),
      paths: twPaths,
      esModule: false
    },
    plugins: [
      resolve({ browser: true }),
      commonjs(),
      terser({
        format: {
          comments: function(node, comment) {
            // Keep TiddlyWiki metadata comments (/*\ ... \*/)
            return comment.type === "comment2" && /^\\/.test(comment.value);
          }
        }
      })
    ]
  };
});
